# Criando uma tupla
tupla = ("a", "b", "c")
print(tupla)
# Adicionando um elemento a uma tupla (impossível)
tupla.append("d")
# Removendo um elemento de uma tupla (impossível)
tupla.remove("c")
# Obtendo o comprimento de uma tupla
len(tupla)
# Iterando sobre uma tupla
for item in tupla:
    print(item)